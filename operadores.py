# Declaring values and organizing them together
num_one = 3
num_two = 5

# Arithmetic operations
total = num_one + num_two
diff = num_two - num_one
product = num_one * num_two
div = num_two / num_one
remainder = num_two % num_one
divfloor = num_two // num_one

# Printing values with label
print('total: ', total)
print('difference: ', diff)
print('product: ', product)
print('division: ', div)
print('remainder: ', remainder)
print('floor division: ', divfloor)

# Comparison operators
print('Is num_one greater than num_two? ', num_one > num_two)
print('Is num_one less than num_two? ', num_one < num_two)
print('Is num_one equal to num_two? ', num_one == num_two)
print('Is num_one not equal to num_two? ', num_one != num_two)
print('Is num_one greater than or equal to num_two? ', num_one >= num_two)
print('Is num_one less than or equal to num_two? ', num_one <= num_two)

# Boolean operators
print('Is num_one greater than 2 AND num_two greater than 2? ', (num_one > 2 and num_two > 2))
print('Is num_one greater than 2 OR num_two greater than 2? ', (num_one > 2 or num_two > 2))
print('NOT (num_one greater than 2 AND num_two greater than 2)? ', not (num_one > 2 and num_two > 2))

print(num_one != num_two)
print( not (num_one != num_two) )

